package entity;

public class StationGeneral {
	public String id;
	public String stationName;
	public String stationAddress;
	public int num_Vehicies;
	public int num_Docks;

	public String getID() {
		return id;
	}
	public String getStationName() {
		return stationName;
	}

	public String getStationAddress() {
		return stationAddress;
	}

	public int getNum_Vehicies() {
		return num_Vehicies;
	}

	public int getNum_Docks() {
		return num_Docks;
	}

}
